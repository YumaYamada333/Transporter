//----------------------------------------------------------------------
//! @brief コンストラクタ
//!
//! @param[in] 初期シーンの番号
//----------------------------------------------------------------------
Game::Game()
{
	srand((unsigned int)time(nullptr));

	//シーンの実体化
	m_scene_manager = new SceneManager(GameBase::TITLE);
}

//----------------------------------------------------------------------
//! @brief デストラクタ
//----------------------------------------------------------------------
Game::~Game()
{
	//シーンの破棄
	delete m_scene_manager;
}

//----------------------------------------------------------------------
//! @brief ゲームの更新処理
//!
//! @param[in] なし
//!
//! @return なし
//----------------------------------------------------------------------
void Game::Update()
{
	//シーンの更新
	m_scene_manager->Update();
}

//----------------------------------------------------------------------
//! @brief ゲームの描画処理
//!
//! @param[in] なし
//!
//! @return なし
//----------------------------------------------------------------------
void Game::Render()
{
	//シーンの描画
	m_scene_manager->Render();
}